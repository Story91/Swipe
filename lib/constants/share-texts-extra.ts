// ============================================
// HELPER: Get random from any array
// ============================================

export function getRandomFromArray<T>(array: T[]): T {
  return array[Math.floor(Math.random() * array.length)];
}

// ============================================
// HELPER: Get seasonal intro
// ============================================

export function getSeasonalIntro(season: keyof typeof SEASONAL_STAKE_INTROS): string {
  return getRandomFromArray(SEASONAL_STAKE_INTROS[season]);
}

